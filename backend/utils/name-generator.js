const generateRandomIndex = (upperBound) => {
  return Math.floor(Math.random() * upperBound)
}

const generateRandomName = () => {
  const adjective = adjectiveArray[generateRandomIndex(adjectiveArray.length)]
  const noun = nounArray[generateRandomIndex(nounArray.length)]
  return adjective.concat(' ', noun)
}

